numbers = [1, 2, 3]

# 1: For loop over list
for n in numbers:
    print(n)

# 2: For loop over set
s = {10, 20, 30}
for n in s:
    print(n)

# 3: For loop over dictionary keys
d = {"a": 1, "b": 2}
for key in d:
    print(key)

# 4: For loop over dictionary values
for value in d.values():
    print(value)

# 5: For loop over dictionary items
for key, value in d.items():
    print(key, value)
